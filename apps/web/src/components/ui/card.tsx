import * as React from "react";
import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { glass?: boolean }
>(({ className, glass = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border border-[hsl(var(--border))] bg-[hsl(var(--card))] text-[hsl(var(--card-foreground))] shadow-sm shadow-black/5 transition-all",
      glass &&
        "bg-[hsl(var(--glass-bg))] backdrop-blur-[var(--glass-blur)] border-[hsl(var(--glass-border))]",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

